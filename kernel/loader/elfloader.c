static int32_t elf_validate(struct Elf64_Ehdr_t *elf_header)
{
	if (memcmp(&elf_header->e_ident, ELFMAGIC, 4))
		return -1;
	if (elf_header->e_ident[EI_CLASS] != ELFCLASS64)
		return -1;
	if (elf_header->e_ident[EI_DATA] != ELFDATA2LSB)
		return -1;
	if (elf_header->e_ident[EI_VERSION] != EV_CURRENT)
		return -1;
	if (elf_header->e_type != ET_EXEC)
		return -1;
	if (elf_header->e_machine != EM_RISCV)
		return -1;
	if (elf_header->e_version != EV_CURRENT)
		return -1;
	if (elf_header->e_phentsize != sizeof(struct Elf64_Phdr_t))
		return -1;
	return 0;
}

static int32_t load_segment(struct mm_struct *mm, struct m_inode_t *inode,
			    struct Elf64_Phdr_t *prgm_header, uint64_t offset)
{
	assert(prgm_header->p_align == PGSIZE);		 // align to page
	assert(OFFSETPAGE(prgm_header->p_vaddr) == 0);	 // align to page

	int32_t perm = PTE_U;
	if (get_var_bit(prgm_header->p_flags, PF_X))
		set_var_bit(perm, PTE_X);
	if (get_var_bit(prgm_header->p_flags, PF_R))
		set_var_bit(perm, PTE_R);
	if (get_var_bit(prgm_header->p_flags, PF_W))
		set_var_bit(perm, PTE_W);

	return add_vm_area(mm, prgm_header->p_vaddr,
			   prgm_header->p_vaddr + prgm_header->p_memsz, perm,
			   offset, (struct file_t *)inode);
}

/**
 * @brief
 *
 * @param inode
 * @return uint64_t
 */
int32_t load_elf(struct proc_t *p, struct m_inode_t *inode)
{
	struct Elf64_Ehdr_t *elf_header = (struct Elf64_Ehdr_t *)inode;
	if (elf_validate(elf_header) != 0)
		return -INVALID_ELFFORMAT;
	struct Elf64_Phdr_t *prgm_header =
		(struct Elf64_Phdr_t *)((char *)inode + elf_header->e_phoff);
	for (int32_t i = 0; i < elf_header->e_phnum; i++, prgm_header++) {
		if (prgm_header->p_type == PT_LOAD) {
			if (load_segment(p->mm, inode, prgm_header,
					 prgm_header->p_offset) == -1)
				return -2;
		}
	}
	p->tf->epc = elf_header->e_entry;
	return 0;
}